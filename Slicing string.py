"""Slicing
You can return a range of characters by using the slice syntax.

Specify the start index and the end index, separated by a colon, to return a part of the string."""

b = "hello, world"
print(b[2:5])


#slice from the start

b = "hello, world"
print(b[:5])

#slice to the end

b = "hello, world"
print(b[2:])